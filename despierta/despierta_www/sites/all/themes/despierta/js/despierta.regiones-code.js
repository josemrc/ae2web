/**
 * Returns the predefined administrative areas.
 *
 * @param country_code
 *   The country code for which administrative areas should be returned.
 *
 * @return
 *   An array of predefined administrative areas for the given country code, or
 *   NULL if not found.
 */
function addressfield_get_administrative_areas(country_code, region_code) {
    var areas;
    var administrative_areas = _addressfield_get_administrative_areas_defaults();
    if ( country_code !== undefined && administrative_areas[country_code] !== undefined ) {
        if (region_code !== undefined ) {
            areas = administrative_areas[country_code][region_code];
        }
        else {
            areas = administrative_areas[country_code];
        }
    }
    return areas;
}

function addressfield_get_region_code(country_code, region_name) {
    var region_code;
    var administrative_areas = _addressfield_get_administrative_areas_defaults();
    if ( country_code !== undefined && administrative_areas[country_code] !== undefined && region_name !== undefined ) {
        for (var idx in administrative_areas[country_code])  {
            var r_name = administrative_areas[country_code][idx];
            if ( region_name == r_name ) {
                region_code = idx;
            }
        }
    }
    return region_code;
}

